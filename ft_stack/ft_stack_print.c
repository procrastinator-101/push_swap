#include "ft_stack.h"

void	ft_stack_print(t_stack *stack)
{
	int	i;

	printf("------------------------------------------------------------\n");
	i = stack->end;
	while (i--)
		printf("%d\n", stack->data[i]);
	printf("------------------------------------------------------------\n");
}
