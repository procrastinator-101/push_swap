#include "stack.h"

void	ft_print_stack(t_stack *stack)
{
	int	i;

	printf("------------------------------------------------------------\n");
	i = stack->size;
	while (i--)
		printf("%d\n", stack->content[i]);
	printf("------------------------------------------------------------\n");
}
