#include "checker.h"

void	ft_verbose(t_stack *a, t_stack *b)
{
	int	i;
	int	j;

	printf("------------------------------------------------------------\n");
	i = a->size - 1;
	j = b->size - 1;
	while (i >= 0 || j >= 0)
	{
		if (i >= 0)
			printf("%-11d", a->content[i]);
		else
			printf("           ");
		if (j >= 0)
			printf("%d", b->content[j]);
		printf("\n");
		i--;
		j--;
	}
	printf("---------- ----------\n");
	printf("    a          b     \n");
	printf("------------------------------------------------------------\n");
}
